package com.ola.controllers;

public class TagController {
    private int node;
    private int tagOrigin;
    private int tagDistance;

    public int getNode() {
        return node;
    }

    public void setNode(int node) {
        this.node = node;
    }

    public int getTagOrigin() {
        return tagOrigin;
    }

    public void setTagOrigin(int tagOrigin) {
        this.tagOrigin = tagOrigin;
    }

    public int getTagDistance() {
        return tagDistance;
    }

    public void setTagDistance(int tagDistance) {
        this.tagDistance = tagDistance;
    }
}
