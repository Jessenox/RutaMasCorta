package com.ola.controllers;

public class Controlador {
    private int nodeBase;
    private int nodeEnded;
    private int nodeDistance;

    public int getNodeBase() {
        return nodeBase;
    }

    public void setNodeBase(int nodeBase) {
        this.nodeBase = nodeBase;
    }

    public int getNodeEnded() {
        return nodeEnded;
    }

    public void setNodeEnded(int nodeEnded) {
        this.nodeEnded = nodeEnded;
    }

    public int getNodeDistance() {
        return nodeDistance;
    }

    public void setNodeDistance(int nodeDistance) {
        this.nodeDistance = nodeDistance;
    }
}
